package com.github.hanyaeger.api.engine.entities.entity;

/**
 * An {@link AnchorPoint} denotes the point of an {@link YaegerEntity} which is being used for
 * placement on the x,y-coordinate.
 */
public enum AnchorPoint {
    TOP_LEFT,
    TOP_CENTER,
    TOP_RIGHT,
    CENTER_LEFT,
    CENTER_CENTER,
    CENTER_RIGHT,
    BOTTOM_LEFT,
    BOTTOM_CENTER,
    BOTTOM_RIGHT;
}
